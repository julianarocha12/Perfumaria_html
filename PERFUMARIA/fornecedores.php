<b>Twoone Onetwo</b> <br>
Empresa fundada em 2012, a Twoone Onetwo possui um catálogo com mais de 100 produtos que são distribuídos em várias linhas diferentes dentro do mercado de estética e beleza.  <br>
Assim todos os produtos possuem, no mínimo, 50% de ingredientes naturais na sua composição, além de utilizar menos água e energia no preparo, fazendo com que a marca se destaque pela responsabilidade ambiental. <br>
Site: https://www.twooneonetwo.com.br/ <br>
