<h1><center>Sobre</center></h1>
<h2>Criação</h2>

A perfumaria Essenciale foi criada no ano de 2015 na cidade de São Paulo, oferece uma extensa linha de perfumes!<br>
Atualmente, a Rede conta com 5 lojas, mais de 35 colaboradores e continua em crescimento. <br>
Essenciale sente-se orgulhosa em ter atraído pessoas que amam o que fazem, vivem a energia do varejo, adoram desafios e buscam melhorar a cada dia.<br>
<h2><br>Objetivo<br></h2>
O objetivo da Essenciale é sempre oferecer produtos de qualidade, priorizar o cliente, sua auto estima e seu bem estar.
<h2><br>Visão<br></h2>
Ser reconhecida como a melhor rede de perfumaria da região, ser eficiente e inovadora, sempre visando o crescimento seguro e sustentável.
